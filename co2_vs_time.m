function co2 = co2_vs_time(obstime);

yg =[ ...
  2002   372.42; ...
  2003   374.97; ...
  2004   376.78; ...
  2005   378.81; ...
  2006   380.94; ...
  2007   382.70; ...
  2008   384.77; ...
  2009   386.28; ...
  2010   388.57; ...
  2011   390.49; ...
  2012   392.47; ...
  2013   395.22; ...
  2014   397.15 ];

% Interp from beginning of 2002, or -0.5 years
year_offset = yg(1,1);
year_data = yg(:,1) - year_offset;

year_offset_dtime = datetime(2002,7,1);

delta_year = years(duration(obstime-year_offset_dtime));

co2 = interp1(year_data,yg(:,2),delta_year,'linear','extrap');

